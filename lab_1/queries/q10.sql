SELECT 
	CAND_NAME,
	CAND_OFFICE_ST,
	SUM(TRANSACTION_AMT) as ttl

FROM
	CANDIDATE,
	INDIV_CONTRIB

WHERE
	CAND_PCC = CMTE_ID
	AND CAND_OFFICE = 'S'
	AND CAND_STATUS IN ('C','N')
	AND CAND_ELECTION_YR = 2016
	AND CAND_OFFICE_ST != STATE
	AND ENTITY_TP = 'IND'

GROUP BY
	CAND_NAME

ORDER BY
	ttl DESC

LIMIT
	5;

